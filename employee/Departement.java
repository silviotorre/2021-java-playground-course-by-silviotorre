import java.util.List;

public class Departement {
    String departementName;
    List<string>assignedEmployeesList;

    public List<string> getAssignedEmployeesList() {
        return this.assignedEmployeesList;
    }

    public void assignEmployee(string s) {
        this.assignedEmployeesList.add(s);
    }

    public String getDepartementName() {
        return this.departementName;
    }

    public void setDepartementName(String departementName) {
        this.departementName = departementName;
    }


    
    
}
